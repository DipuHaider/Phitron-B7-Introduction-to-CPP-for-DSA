#include<bits/stdc++.h>
using namespace std;

int main() {
    
    string str1 = "Hello";
    string str2 = "World";
    string str = str1 + " " + str2; // Concatenating strings
    cout << str << endl;

    if (str1 == str2) {
        cout << "Strings are equal" << endl;
    } else {
        cout << "Strings are not equal" << endl;
    }

    return 0;
}

