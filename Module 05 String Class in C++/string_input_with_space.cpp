#include<bits/stdc++.h>
using namespace std;

int main() {
    
    int x;
    cin >> x; // Reading an integer input
    string str;
    cin.ignore(); // To ignore the newline character after reading integer input
    getline(cin, str); // Using getline to read a line of input including spaces
    cout << str << endl; // Output the string with spaces
    cout << x << endl; // Output the integer input


    return 0;
}

